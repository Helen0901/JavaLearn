/**
 * 問題

数列9、99、999...9999999999の和を計算するJavaプログラムを書いてください。
この数列は各項が前の項に10を掛けて9を加えることで生成されます。
ループを使用してこの数列を生成し、同時に和を求めてください。

入力

この問題では入力は必要ありません。

出力

計算された数列の和。
 */
package com.ct01;

public class Java18 {
    public static void main(String[] args) {
        // 合計を格納する変数を初期化します
        long sum = 0;

        // 初期の数値を設定します
        long number = 9;

        // 10回の繰り返しを行います
        for (int i = 1; i <= 10; i++) {
            // 合計に現在の数値を加えます
            sum += number;

            // 次の数値を生成します（前の数値に10を掛けて9を足す）
            number = number * 10 + 9;
        }

        // 最終的な合計を出力します
        System.out.println(sum);
    }
}
